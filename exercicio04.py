#Faça um Programa que verifique se uma letra digitada é vogal ou consoante. 

letra = input() 

if letra == 'a': 
    print("essa letra é vogal") 
elif letra == 'e': 
    print("essa letra é vogal") 
elif letra == 'i': 
    print("essa letra é vogal") 
elif letra == 'o': 
    print("essa letra é vogal") 
elif letra == 'u': 
    print("essa letra é vogal") 
else: 
    print('essa letra é uma consoante') 

